def perevirka4(x, y):
    i = 1
    m = False
    while x // i != 0:
        z = (x // i) % 10
        if z == y:
            print("Цифра {} входить в число {}".format(y, x))
            m = True
        i *= 10
    if m == False:
        print("Цифра %u НЕ входить в число %u" % (y, x))
         
