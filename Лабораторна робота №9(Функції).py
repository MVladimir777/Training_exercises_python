def perevirka1(a, b, c):
    if (a <= 0) or (b <= 0) or (c <= 0):
        print("Не існує відємної або нульової сторони.")
    elif (a + b <= c) or (a + c <= b) or (c + b <= a):
        print("З відрізків такої довжини утворити трикутник неможливо.")
    elif (a ** 2 + b ** 2 == c ** 2) or (a ** 2 + c ** 2 == b ** 2) or (c ** 2 + b ** 2 == a ** 2):
        print("Прямокутний трикутник")
    elif (a ** 2 + b ** 2 < c ** 2) or (a ** 2 + c ** 2 < b ** 2) or (c ** 2 + b ** 2 < a ** 2):
        print("Тупокутній трикутник")
    elif (a ** 2 + b ** 2 > c ** 2) or (a ** 2 + c ** 2 > b ** 2) or (c ** 2 + b ** 2 > a ** 2):
        print("Гострокутний трикутник")

print('''1. Перевірити (у функції), чи існує трикутник із заданими сторонами
 a, b, c. Якщо так, то визначити, який він: гострокутний, прямокутний, 
 тупокутний.''')

x = float(input("Сторона a = "))
y = float(input("Сторона b = "))
z = float(input("Сторона c = "))
perevirka1(x, y, z)


